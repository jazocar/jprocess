package cl.jazocar.jprocess.exception;

public class JProcessFrontendException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public JProcessFrontendException() {
		super();
	}

	public JProcessFrontendException(String message, Throwable cause) {
		super(message, cause);
	}

	public JProcessFrontendException(String message) {
		super(message);
	}

	public JProcessFrontendException(Throwable cause) {
		super(cause);
	}
	
}
